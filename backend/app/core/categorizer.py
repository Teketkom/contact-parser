def _normalize(text: str) -> str:
    """Приводит строку к нижнему регистру, убирает лишние пробелы."""
    return re.sub(r"\s+", " ", text.strip().lower())


def categorize_contact(position: str | None) -> str:
    """
    Возвращает название CRM-категории для указанной должности.

    Алгоритм:
    1. Точное совпадение (case-insensitive) с эталонными вариантами.
    2. Нечёткий поиск: позиция СОДЕРЖИТ ключевую фразу/слово из правил.
    3. Ключевые слова широкого охвата.
    4. Если ничего не совпало → «Другие категории».
    """
    if not position or not position.strip():
        return DEFAULT_CATEGORY

    normalized = _normalize(position)

    # 1. Точное совпадение
    if normalized in _EXACT_INDEX:
        return _EXACT_INDEX[normalized]

    # 2 + 3. Нечёткое сопоставление по правилам (в порядке убывания специфичности)
    for pattern, category in _FUZZY_RULES:
        if pattern.search(normalized):
            return category

    return DEFAULT_CATEGORY


def categorize_contacts(contacts: list[ContactRecord]) -> dict[str, list[ContactRecord]]:
    """
    Принимает список ContactRecord, возвращает словарь category → [contacts].

    Гарантии:
    - Включает только категории, в которых есть хотя бы один контакт.
    - Всегда включает «Другие категории» (даже если пуст), чтобы caller
      мог явно проверить некатегоризированные записи.
    """
    result: dict[str, list[ContactRecord]] = {}

    for contact in contacts:
        position = contact.position_normalized or contact.position_raw
        category = categorize_contact(position)
        result.setdefault(category, []).append(contact)

    # Гарантируем наличие ключа для некатегоризированных
    result.setdefault(DEFAULT_CATEGORY, [])

    return result
